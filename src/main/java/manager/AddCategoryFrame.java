    private void okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okActionPerformed
        // TODO add your handling code here:
        if (CategoryPanel.getReference().isCategory(nameOfCategory.getText())==true){
             nameOfCategory.setText("");
             JOptionPane.showMessageDialog(null, " Category with such name exists !");
        }
        else{
                  try {

           Manager.getThread().addCategory(nameOfCategory.getText());          
         
         }
          catch (IOException ex) {
            Logger.getLogger(AddCategoryFrame.class.getName()).log(Level.SEVERE, null, ex);
        }   
         CategoryPanel.getReference().addCategoryItem(nameOfCategory.getText());
         CategoryPanel.getReference().setTextOfLable(nameOfCategory.getText());
         CategoryPanel.getReference().setModelData(null);
         CategoryPanel.getReference().doLastItem();
         JOptionPane.showMessageDialog(null, " Add new category  -   " +nameOfCategory.getText()+" !");
         CategoryPanel.getReference().setVisible(true);
         HistoryPanel.getReference().setVisible(false);
         PersonnelPanel.getReference().setVisible(false);
         dispose();
         
        }
        
    }//GEN-LAST:event_okActionPerformed

